		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Hotel_app.Model.X_update> DataTableToList(DataTable dt)
		{
			List<Hotel_app.Model.X_update> modelList = new List<Hotel_app.Model.X_update>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Hotel_app.Model.X_update model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new Hotel_app.Model.X_update();
					if(dt.Rows[n]["id"]!=null && dt.Rows[n]["id"].ToString()!="")
					{
						model.id=int.Parse(dt.Rows[n]["id"].ToString());
					}
					if(dt.Rows[n]["yydh"]!=null && dt.Rows[n]["yydh"].ToString()!="")
					{
					model.yydh=dt.Rows[n]["yydh"].ToString();
					}
					if(dt.Rows[n]["qymc"]!=null && dt.Rows[n]["qymc"].ToString()!="")
					{
					model.qymc=dt.Rows[n]["qymc"].ToString();
					}
					if(dt.Rows[n]["downloadURL"]!=null && dt.Rows[n]["downloadURL"].ToString()!="")
					{
					model.downloadURL=dt.Rows[n]["downloadURL"].ToString();
					}
					if(dt.Rows[n]["preVersion"]!=null && dt.Rows[n]["preVersion"].ToString()!="")
					{
					model.preVersion=dt.Rows[n]["preVersion"].ToString();
					}
					if(dt.Rows[n]["remoteUrl"]!=null && dt.Rows[n]["remoteUrl"].ToString()!="")
					{
					model.remoteUrl=dt.Rows[n]["remoteUrl"].ToString();
					}
					modelList.Add(model);
				}
			}
			return modelList;
		}

